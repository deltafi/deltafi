package org.deltafi.core.types.leader;

/**
 * Connection state for a member site in leader-member monitoring.
 * Indicates whether data is current, cached, or unavailable.
 */
public enum ConnectionState {
    CONNECTED,      // Successfully retrieved data
    UNREACHABLE,    // Network error, timeout, or HTTP error
    STALE           // Data is cached from previous successful poll
}
